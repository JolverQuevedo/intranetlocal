SELECT DISTINCT VP.PO, PDD.COLOR
FROM		VIEW_POS		AS VP 
INNER JOIN	[PO-DETA]		AS PDD ON PDD.PO = VP.PO
INNER JOIN  PO_PARTIDA		AS PPO ON PPO.PO = PDD.PO AND PDD.UBI = PPO.ubi
inner join  EXPLOSION_TELA	as ET ON ET.OFI=VP.OFI AND ET.PO = VP.PO
INNER JOIN	CCTELA			AS CCT ON CCT.CCT = PPO.CCT  AND ET.TELA= CCT.TELA + CCT.COLOR + CCT.COMBI 
WHERE VP.CODEST = '00006' AND VP.TEMPORADA = '035' AND VP.CLI = '00001'
