-- COLORES DE COMBOS DEL CLIENTE, TEMPORADA, ESTILO QUE TIENEN EXPLOSION DE TELA
select  DISTINCT -- VP.po, VP.cli, VP.codest,VP.temporada, 
ET.COMBO--, ET.TELA
FROM VIEW_POS as vp
inner join EXPLOSION_TELA as ET ON ET.OFI=VP.OFI AND ET.PO = VP.PO
INNER JOIN PO_PARTIDA AS PPO ON PPO.PO = ET.PO
INNER JOIN ASIGNA_CCT AS ACC ON ACC.CCT = PPO.CCT
where vp.cli = '00001' and vp.codest= '00002' and vp.temporada = '035'

