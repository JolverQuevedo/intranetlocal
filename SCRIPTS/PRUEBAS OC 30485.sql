Select OC_CCODIGO AS COD ,OC_CDESREF AS DESCRI, OC_CUNIDAD AS UNI, OC_NCANORD AS ORDEN,
OC_NPREUNI AS UNI_OC,
(OC_NCANORD*OC_NPREUNI) AS TOT_OC,
CANTI = case when LTRIM(RTRIM(C5_CTIPMOV)) ='E' THEN C6_NCANTID ELSE C6_NCANTID *-1 END, 
ltrim(rtrim(c6_ctd)) + ' - '+ ltrim(rtrim(c6_cnumdoc)) as DOC, 
(C6_NPREUNI) AS UNI_PE,
CONVERT(VARCHAR, C5_DFECDOC, 103) AS FECHA
From RSFACCAR..CO0001MOVD AS CC 
 inner join  RSFACCAR..al0001movc AS A  on OC_CNUMORD = c5_cnumord
inner join RSFACCAR..al0001movd AS B on c5_cnumdoc = c6_cnumdoc and c5_ctd = c6_ctd and c5_calma = c6_calma 
and OC_CITEM = C6_CITEMOC
 Where OC_CNUMORD='0000030485' AND OC_CCODPRO='20101635440' AND OC_NCANORD > 0 ORDER BY OC_CCODIGO,C5_DFECDOC desc, OC_CITEM ;  

